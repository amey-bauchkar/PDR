    coalesce(p_contact->>'email', ''),
    coalesce(p_contact->>'phone', ''),
    coalesce(p_contact->>'company', ''),
    coalesce(p_contact->>'inquiryType', ''),
    coalesce(p_contact->>'message', ''),
