create or replace function public.sync_quote_session(p_session_hash text, p_items jsonb default '[]'::jsonb)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_session_id uuid;
  v_item_count integer;
  v_total_quantity integer;
begin
  v_item_count := coalesce(jsonb_array_length(coalesce(p_items, '[]'::jsonb)), 0);
  select coalesce(sum(greatest(1, coalesce((item->>'qty')::integer, 1))), 0)
    into v_total_quantity
  from jsonb_array_elements(coalesce(p_items, '[]'::jsonb)) as item;

  insert into public.quote_sessions (session_hash, status, item_count, total_quantity, last_synced_at, updated_at)
  values (p_session_hash, 'open', v_item_count, v_total_quantity, now(), now())
  on conflict (session_hash) do update
    set status = 'open',
        item_count = excluded.item_count,
        total_quantity = excluded.total_quantity,
        last_synced_at = now(),
        updated_at = now()
  returning id into v_session_id;

  delete from public.quote_session_items where session_id = v_session_id;

  insert into public.quote_session_items (
    session_id,
    line_number,
    product_slug,
    product_title,
    product_specs,
    image_url,
    quantity,
    item_hash
  )
  select
    v_session_id,
    ordinality::integer,
    nullif(item->>'slug', ''),
    coalesce(item->>'title', ''),
    coalesce(item->>'specs', ''),
    coalesce(item->>'image', ''),
    greatest(1, coalesce((item->>'qty')::integer, 1)),
    encode(digest(p_session_hash || ':' || coalesce(item->>'title', '') || ':' || coalesce(item->>'specs', '') || ':' || ordinality::text, 'sha256'), 'hex')
  from jsonb_array_elements(coalesce(p_items, '[]'::jsonb)) with ordinality as item_rows(item, ordinality);

  return v_session_id;
end;
$$;

create or replace function public.submit_quote_request(p_session_hash text, p_contact jsonb, p_items jsonb default '[]'::jsonb)
returns uuid
language plpgsql
security definer
set search_path = public
as $$
declare
  v_session_id uuid;
  v_request_id uuid;
  v_item_count integer;
  v_total_quantity integer;
  v_full_name text;
  v_email text;
  v_company text;
  v_notes text;
begin
  v_session_id := public.sync_quote_session(p_session_hash, p_items);

  v_item_count := coalesce(jsonb_array_length(coalesce(p_items, '[]'::jsonb)), 0);
  select coalesce(sum(greatest(1, coalesce((item->>'qty')::integer, 1))), 0)
    into v_total_quantity
  from jsonb_array_elements(coalesce(p_items, '[]'::jsonb)) as item;

  v_full_name := coalesce(p_contact->>'name', '');
  v_email := coalesce(p_contact->>'email', '');
  v_company := coalesce(p_contact->>'company', '');
  v_notes := coalesce(p_contact->>'notes', '');

  insert into public.quote_requests (
    session_id,
    session_hash,
    full_name,
    email,
    company,
    notes,
    item_count,
    total_quantity,
    status,
    created_at
  )
  values (
    v_session_id,
    p_session_hash,
    v_full_name,
    v_email,
    v_company,
    v_notes,
    v_item_count,
    v_total_quantity,
    'received',
    now()
  )
  returning id into v_request_id;

  insert into public.quote_request_items (
    request_id,
    line_number,
    product_slug,
    product_title,
    product_specs,
    image_url,
    quantity,
    item_hash
  )
  select
    v_request_id,
    ordinality::integer,
    nullif(item->>'slug', ''),
    coalesce(item->>'title', ''),
    coalesce(item->>'specs', ''),
    coalesce(item->>'image', ''),
    greatest(1, coalesce((item->>'qty')::integer, 1)),
    encode(digest(p_session_hash || ':request:' || coalesce(item->>'title', '') || ':' || coalesce(item->>'specs', '') || ':' || ordinality::text, 'sha256'), 'hex')
  from jsonb_array_elements(coalesce(p_items, '[]'::jsonb)) with ordinality as item_rows(item, ordinality);

  update public.quote_sessions
    set status = 'submitted',
        submitted_at = now(),
        updated_at = now()
  where id = v_session_id;

  return v_request_id;
end;
$$;

